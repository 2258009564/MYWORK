signed main()
{
    int t;
    cin >> t;
    int sz0, sz1, k;
    string s;
    while (t--)
    {
        s.clear();
        bool flag = 1;

        cin >> sz0 >> sz1 >> k;

        if (k == 0)
        {
            if (sz1 and sz0)
            {
                cout << -1 << endl;
                continue;
            }
        }

        int sz = k + 1;
        if (sz % 2 == 0)
        {
            sz0 -= sz / 2;
            sz1 -= sz / 2;

            if (sz0 < 0 or sz1 < 0)
            {
                flag = 0;
            }
            if (flag)
            {
                for (int i = 1; i <= sz0; i++)
                {
                    s += '0';
                }

                for (int i = 1; i <= sz / 2; i++)
                {
                    s += "01";
                }

                for (int i = 1; i <= sz1; i++)
                {
                    s += '1';
                }
            }
        }
        else
        {
            sz -= 1;
            sz0 -= sz / 2;
            sz1 -= sz / 2;

            if (sz0 < 0 or sz1 < 0)
            {
                flag = 0;
            }
            if (flag)
            {
                for (int i = 1; i <= sz / 2; i++)
                {
                    s += "01";
                }

                for (int i = 1; i <= sz1; i++)
                {
                    s += '1';
                }

                for (int i = 1; i <= sz0; i++)
                {
                    s += '0';
                }

                if (s.size() > 0 && s[s.size() - 1] == '1' && sz0 > 0)
                {
                    flag = 0;
                }
            }
        }

        cout << (flag ? s : "-1") << endl;
    }
}