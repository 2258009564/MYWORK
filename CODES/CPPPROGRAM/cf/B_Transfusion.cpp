// 通用版本的 >> 重载，用于任意类型的 std::vector<T>
template <typename T>
istream &operator>>(istream &in, vector<T> &v)
{
    for (auto &x : v)
    {
        in >> x;
    }
    return in;
}

// 通用版本的 << 重载，用于任意类型的 std::vector<T>
template <typename T>
ostream &operator<<(ostream &out, const vector<T> &v)
{
    for (const auto &x : v)
    {
        out << x << ' ';
    }
    return out;
}

void solve()
{
    int n;
    int total0 = 0, total1 = 0, sum0 = 0, sum1 = 0;

    cin >> n;
    vi v(n);
    cin >> v;
    for (int i = 0; i < n; i++)
    {
        if (i % 2 == 0)
        {
            total0 += v[i];
            sum0++;
        }
        else
        {
            total1 += v[i];
            sum1++;
        }
    }
    if (total0 * sum1 == total1 * sum0 and total0 % sum0 == 0 and total1 % sum1 == 0)
    {
        cout << "YES" << endl;
    }
    else
    {
        cout << "NO" << endl;
    }
}

signed main()
{
    ios::sync_with_stdio(false), cin.tie(0), cout.tie(0);
    int TT = 1;
    cin >> TT;
    while (TT--)
    {
        solve();
    }
}