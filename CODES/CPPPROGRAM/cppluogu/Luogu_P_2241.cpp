void solve()
{
    int n, m;
    cin >> n >> m;
    int ans1 = 0, ans2 = 0;
    for (int i = 1; i <= n; i++)
    {
        for (int j = 1; j <= m; j++)
        {
            ans1 += i == j;
            ans2 += i != j;
        }
    }
    cout << ans1 << ' ' << ans2;
}

signed main()
{
    cin.tie(0)->ios::sync_with_stdio(0);
    cout << setiosflags(ios::fixed) << setprecision(2);
    int TT = 1;
    // cin >> TT;
    while (TT--)
    {
        solve();
        cout << endl;
    }
}